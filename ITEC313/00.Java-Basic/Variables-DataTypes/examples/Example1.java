// Example1.java
// Demonstrates primitive data types in Java
// Python comparison: In Python, variables are dynamically typed; in Java, you must declare the type.

public class Example1 {
    public static void main(String[] args) {
        // Integer type
        int age = 25; // In Python: age = 25
        // Double type
        double salary = 55000.50; // In Python: salary = 55000.50
        // Boolean type
        boolean isStudent = false; // In Python: is_student = False
        // Character type
        char grade = 'A'; // In Python: grade = 'A'

        // Print variables
        System.out.println("Age: " + age);
        System.out.println("Salary: " + salary);
        System.out.println("Is Student: " + isStudent);
        System.out.println("Grade: " + grade);
    }
} 