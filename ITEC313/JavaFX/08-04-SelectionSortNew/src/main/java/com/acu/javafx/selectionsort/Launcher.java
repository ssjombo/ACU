package com.acu.javafx.selectionsort;

import javafx.application.Application;

/**
 * Launcher class for the Selection Sort Visualizer JavaFX application.
 * This class is responsible for starting the JavaFX application.
 * 
 * @author ACU
 * @version 1.0.0
 */
public class Launcher {
    
    /**
     * Main method that launches the JavaFX application.
     * 
     * @param args command line arguments
     */
    public static void main(String[] args) {
        Application.launch(SelectionSortApp.class, args);
    }
}
